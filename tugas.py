#buatlah rumus menghitung luas Persegi
sisi = 6
luasPersegi = sisi * sisi
print("Luas Persegi", luasPersegi)

#buatlah rumus menghitung luas Persegi Panjang
panjang = 7
lebar = 5
luasPersegiPanjang = panjang * lebar
print("Luas Persegi Panjang", luasPersegiPanjang)

#buatlah rumus menghitung luas Segitiga
alas = 6
tinggi = 9
luasSegitiga = alas * tinggi / 2
print("Luas Segitiga", luasSegitiga)

#buatlah rumus menghitung luas Lingkaran
jari2 = 7
diameter = 14
PHI = 3.14
luasLingkaran1 = PHI * jari2 * jari2
luasLingkaran2 = PHI * diameter * diameter / 4
print("Luas Lingkaran 1", luasLingkaran1,
    "\nLuas Lingkaran 2", luasLingkaran2)

